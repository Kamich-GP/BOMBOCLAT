    but1 = types.InlineKeyboardButton(text='–ù–∞–∑–∞–¥üîô', callback_data='back')
    but2 = types.InlineKeyboardButton(text='–û—Ñ–æ—Ä–º–∏—Ç—å –∑–∞–∫–∞–∑üßæ', callback_data='order')
    but3 = types.InlineKeyboardButton(text='–û—á–∏—Å—Ç–∏—Ç—å –∫–æ—Ä–∑–∏–Ω—ÉüóëÔ∏è', callback_data='clear')
